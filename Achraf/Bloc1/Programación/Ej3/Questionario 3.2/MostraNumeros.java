public class MostraNumeros {

    public static void main(String[] args) {
        int i = 1; // Inicialitzem el contador a 1
        
        while (i <= 5) { // Continuem mentre i sigui menor o igual a 5
            System.out.print(i + " "); // Imprimim el número i un espai
            i++; // Incrementem el contador
        }
    }
}